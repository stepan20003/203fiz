let currentTheme = null;
let currentIndex = 0;
let score = 0;

const themeSelector = document.getElementById('theme-selector');
const quizContainer = document.getElementById('quiz');
const nextBtn = document.getElementById('next');
const resultContainer = document.getElementById('result');

Object.keys(allThemes).forEach(theme => {
  const btn = document.createElement('button');
  btn.innerText = theme;
  btn.onclick = () => startTheme(theme);
  themeSelector.appendChild(btn);
});

function startTheme(theme) {
  currentTheme = theme;
  currentIndex = 0;
  score = 0;
  themeSelector.style.display = 'none';
  nextBtn.style.display = 'inline-block';
  loadQuestion();
}

function loadQuestion() {
  const q = allThemes[currentTheme][currentIndex];
  quizContainer.innerHTML = `<h2>${q.question}</h2>`;
  q.answers.forEach((ans, index) => {
    const btn = document.createElement('div');
    btn.className = 'answer';
    btn.innerText = ans;
    btn.onclick = () => selectAnswer(index, btn);
    quizContainer.appendChild(btn);
  });
}

function selectAnswer(index, el) {
  const correctIndex = allThemes[currentTheme][currentIndex].correct;
  const answers = document.querySelectorAll('.answer');
  answers.forEach((btn, i) => {
    btn.onclick = null;
    if (i === correctIndex) btn.classList.add('correct');
    else if (i === index) btn.classList.add('incorrect');
  });
  if (index === correctIndex) score++;
}

nextBtn.onclick = () => {
  currentIndex++;
  if (currentIndex < allThemes[currentTheme].length) {
    loadQuestion();
  } else {
    quizContainer.innerHTML = '';
    nextBtn.style.display = 'none';
    resultContainer.innerText = `Թեմա «${currentTheme}» — Քեզ հաջողվեց ճիշտ պատասխանել ${score} / ${allThemes[currentTheme].length} հարցերին։`;
    themeSelector.style.display = 'block';
  }
};
